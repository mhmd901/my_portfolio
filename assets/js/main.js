const titles = ["Web Designer", "Web Developer", "Mobile Developer", "Robotics Trainer"];
let titleIndex = 0;
let charIndex = 0;
const typingElement = document.getElementById("changing-title");

function type() {
    if (charIndex < titles[titleIndex].length) {
        typingElement.textContent += titles[titleIndex].charAt(charIndex);
        charIndex++;
        setTimeout(type, 100);
    } else {
        setTimeout(erase, 2000);
    }
}

function erase() {
    if (charIndex > 0) {
        typingElement.textContent = titles[titleIndex].substring(0, charIndex - 1);
        charIndex--;
        setTimeout(erase, 50);
    } else {
        titleIndex = (titleIndex + 1) % titles.length;
        setTimeout(type, 300);
    }
}

document.addEventListener("DOMContentLoaded", function () {
    setTimeout(type, 500);
});
